/* Precomputed magic constants for efficient division by a constant */
struct Divisor {
    uint16_t divisor;
    uint8_t shift;
    uint8_t unused;
    float recip;
    uint64_t multiplier;

    uint64_t operator()(uint64_t value) const {
        __uint128_t xl = multiplier, yl = value;
        __uint128_t rl = xl * yl;
        uint64_t q = (uint64_t) (rl >> 64);
        uint64_t t = ((value - q) >> 1) + q;
        return t >> shift;
    }

    template <size_t Size> simd::Array<uint64_t, Size> operator()(simd::Array<uint64_t, Size> value) const {
        using UInt64Array = simd::Array<uint64_t, Size>;
        UInt64Array q = simd::mulHi(value, UInt64Array::Constant(multiplier));
        return (simd::sri<1>(value - q) + q) >> shift;
    }
};

